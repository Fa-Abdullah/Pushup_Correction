mp_pose = mp.solutions.pose

# Base path for the downloaded dataset
BASE_DATASET_DIR = '/root/.cache/kagglehub/datasets/mohamadashrafsalama/pushup/versions/1'
OUTPUT_CSV = '/content/dataset/landmarks.csv' # Ensure '/content/dataset' exists or create it

# Create the output directory if it doesn't exist
