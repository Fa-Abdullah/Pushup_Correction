import cv2
import mediapipe as mp
import numpy as np
import pandas as pd
from sklearn.ensemble import RandomForestClassifier
from collections import deque

# ----------------------
# 1️⃣ Load your trained model
# ----------------------
# لو خزنتيه باستخدام pickle:
import pickle
with open('pushup_model.pkl', 'rb') as f:
    model = pickle.load(f)

# ----------------------
# 2️⃣ Initialize MediaPipe Pose
# ----------------------
mp_pose = mp.solutions.pose
pose = mp_pose.Pose(static_image_mode=False, min_detection_confidence=0.5)

# ----------------------
# 3️⃣ Real-time smoothing
# ----------------------
queue_size = 5
pred_queue = deque(maxlen=queue_size)

# ----------------------
# 4️⃣ Open webcam
# ----------------------
cap = cv2.VideoCapture(0)  # 0 = default webcam

while True:
    ret, frame = cap.read()
    if not ret:
        break

    # Flip image horizontally for natural selfie view
    frame = cv2.flip(frame, 1)

    # Convert BGR to RGB
    img_rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)

    # Process frame
    results = pose.process(img_rgb)

    # Prepare landmarks for model
    landmarks_list = []
    if results.pose_landmarks:
        for lm in results.pose_landmarks.landmark:
            landmarks_list += [lm.x, lm.y, lm.z, lm.visibility]
        # Predict
        pred = model.predict([landmarks_list])[0]
    else:
        pred = "No person detected"

    # ----------------------
    # Smoothing predictions
    # ----------------------
    pred_queue.append(pred)
    if len(pred_queue) == queue_size:
        # Most frequent prediction in last N frames
        smoothed_pred = max(set(pred_queue), key=pred_queue.count)
    else:
        smoothed_pred = pred

    # ----------------------
    # Draw prediction on frame
    # ----------------------
    cv2.putText(frame, f'Prediction: {smoothed_pred}', (30,50),
                cv2.FONT_HERSHEY_SIMPLEX, 1, (0,255,0), 2)

    # Draw landmarks
    if results.pose_landmarks:
        mp.solutions.drawing_utils.draw_landmarks(
            frame, results.pose_landmarks, mp_pose.POSE_CONNECTIONS)

    # Show frame
    cv2.imshow("Pushup Detection", frame)

    # Break on 'q' key
    if cv2.waitKey(1) & 0xFF == ord('q'):
        break

cap.release()
cv2.destroyAllWindows()

