# إعداد CSV header
n_landmarks = 33  # MediaPipe Pose
header = ['video_name', 'frame_index', 'label', 'detected']
for i in range(n_landmarks):
    header += [f'x_{i}', f'y_{i}', f'z_{i}', f'vis_{i}']

with open(OUTPUT_CSV, 'w', newline='') as f:
    writer = csv.writer(f)
    writer.writerow(header)

    # Define the subdirectories containing the videos and their corresponding labels
    video_subdirs = {
        'Correct sequence': 'correct_pushup',
        'Wrong sequence': 'wrong_pushup'
    }

    # نفّذ على كل فيديو في كل مجلد
    for sub_dir_name, label_prefix in video_subdirs.items():
        current_videos_dir = os.path.join(BASE_DATASET_DIR, sub_dir_name)

        if not os.path.isdir(current_videos_dir):
            print(f"Warning: Directory '{current_videos_dir}' not found. Skipping.")
            continue

        for fname in os.listdir(current_videos_dir):
            if not fname.lower().endswith(('.mp4', '.avi', '.mov', '.mkv')):
                continue
            video_path = os.path.join(current_videos_dir, fname)
            label = label_prefix # Use the label based on the subdirectory
            cap = cv2.VideoCapture(video_path)
            total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

            if total_frames == 0:
                print(f"Warning: No frames found in {video_path}. Skipping.")
                cap.release()
                continue

            with mp_pose.Pose(static_image_mode=False, min_detection_confidence=0.5) as pose:
                frame_idx = 0
                pbar = tqdm(total=total_frames, desc=f"{label_prefix}/{fname}")
                while True:
                    ret, frame = cap.read()
                    if not ret:
                        break
                    # optional: sample frames, e.g., if frame_idx % 2 != 0: continue
                    image_rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
                    results = pose.process(image_rgb)

                    row = [fname, frame_idx, label, 0]  # detected flag default 0
                    if results.pose_landmarks:
                        row[3] = 1
                        for lm in results.pose_landmarks.landmark:
                            row += [lm.x, lm.y, lm.z, lm.visibility]
                    else:
                        # add placeholders for undetected poses
                        for _ in range(n_landmarks):
                            row += [None, None, None, None]

                    writer.writerow(row)
                    frame_idx += 1
                    pbar.update(1)
                pbar.close()
            cap.release()

